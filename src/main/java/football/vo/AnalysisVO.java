package football.vo;

public class AnalysisVO {
	
	private String result;
	
	private String winDrawLose;
	
	private String winPer;
	
	private String goalFor;
	
	private String goalAgainst;
	
	private String avgFor;
	
	private String avgAgainst;
	
	private String avgForAgainst;

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getWinDrawLose() {
		return winDrawLose;
	}

	public void setWinDrawLose(String winDrawLose) {
		this.winDrawLose = winDrawLose;
	}

	public String getWinPer() {
		return winPer;
	}

	public void setWinPer(String winPer) {
		this.winPer = winPer;
	}

	public String getGoalFor() {
		return goalFor;
	}

	public void setGoalFor(String goalFor) {
		this.goalFor = goalFor;
	}

	public String getGoalAgainst() {
		return goalAgainst;
	}

	public void setGoalAgainst(String gaolAgainst) {
		this.goalAgainst = gaolAgainst;
	}

	public String getAvgFor() {
		return avgFor;
	}

	public void setAvgFor(String avgFor) {
		this.avgFor = avgFor;
	}

	public String getAvgAgainst() {
		return avgAgainst;
	}

	public void setAvgAgainst(String avgAgainst) {
		this.avgAgainst = avgAgainst;
	}

	public String getAvgForAgainst() {
		return avgForAgainst;
	}

	public void setAvgForAgainst(String avgForAgainst) {
		this.avgForAgainst = avgForAgainst;
	}
	
}
